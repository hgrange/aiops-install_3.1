source ./tools/0_global/99_config-global.sh
get_kafkacat

for actFile in $(ls -1 $WORKING_DIR_EVENTS | grep "json"); 
do 

#--------------------------------------------------------------------------------------------------------------------------------------------
#  Prepare the Events Data
#--------------------------------------------------------------------------------------------------------------------------------------------


  export EVENTS_TOPIC="alerts-noi-1000-1000"
  export DATE_FORMAT_EVENTS="+%Y-%m-%dT%H:%M:%S"

    #--------------------------------------------------------------------------------------------------------------------------------------------
    #  Create file and structure in /tmp
    #--------------------------------------------------------------------------------------------------------------------------------------------
    mkdir /tmp/training-events/  >$log_output_path 2>&1  || true
    rm /tmp/training-events/x* >$log_output_path 2>&1  || true
    cp $WORKING_DIR_EVENTS/$actFile /tmp/training-events/
    cd /tmp/training-events/

    #--------------------------------------------------------------------------------------------------------------------------------------------
    #  Update Timestamps
    #--------------------------------------------------------------------------------------------------------------------------------------------
    echo "    🔨 Updating Timestamps (this can take several minutes)"
    echo "" > /tmp/timestampedEvents.json
    while IFS= read -r line
    do
        # Get timestamp in ELK format
        export my_timestamp=$(date $DATE_FORMAT_EVENTS)
        # Replace in line
        line=${line//MY_TIMESTAMP/$my_timestamp}
        # Write line to temp file
        echo $line >> /tmp/timestampedEvents.json
        sleep 1
        echo "."
    done < "$actFile"
    echo "      ✅ OK"

    #--------------------------------------------------------------------------------------------------------------------------------------------
    #  Split the files in 1500 line chunks for kafkacat
    #--------------------------------------------------------------------------------------------------------------------------------------------
    echo "    🔨 Splitting"
    split -l 1500 /tmp/timestampedEvents.json
    export NUM_FILES=$(ls | wc -l)
    rm $actFile
    rm /tmp/timestampedEvents.json
    cd -  >$log_output_path 2>&1  || true
    echo "      ✅ OK"



    #--------------------------------------------------------------------------------------------------------------------------------------------
    #  Get the cert for kafkacat
    #--------------------------------------------------------------------------------------------------------------------------------------------
    oc project $WAIOPS_NAMESPACE >$log_output_path 2>&1  || true

    echo "***************************************************************************************************************************************************"
    echo "🥇 Getting Certs"
    echo "***************************************************************************************************************************************************"
    oc extract secret/strimzi-cluster-cluster-ca-cert -n $WAIOPS_NAMESPACE --keys=ca.crt >$log_output_path 2>&1  || true
    echo "      ✅ OK"



    #--------------------------------------------------------------------------------------------------------------------------------------------
    #  Inject the Events
    #--------------------------------------------------------------------------------------------------------------------------------------------
    echo "***************************************************************************************************************************************************"
    echo "🌏  Injecting Events from File: ${actFile}" 
    echo "     Quit with Ctrl-Z"
    echo "***************************************************************************************************************************************************"
    ACT_COUNT=0
    for FILE in /tmp/training-events/*; do 
        if [[ $FILE =~ "x"  ]]; then
            ACT_COUNT=`expr $ACT_COUNT + 1`
            echo "Injecting file ($ACT_COUNT/$(($NUM_FILES-1))) - $FILE"
            ${KAFKACAT_EXE} -v -X security.protocol=SSL -X ssl.ca.location=./ca.crt -X sasl.mechanisms=SCRAM-SHA-512  -X sasl.username=token -X sasl.password=$KAFKA_PASSWORD -b $KAFKA_BROKER -P -t $EVENTS_TOPIC -l $FILE   >$log_output_path 2>&1  || true
            echo "      ✅ OK"
        fi
    done
done

